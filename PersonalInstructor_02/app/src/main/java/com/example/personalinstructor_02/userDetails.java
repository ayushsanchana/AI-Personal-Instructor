package com.example.personalinstructor_02;

public class userDetails {

    String name;
    String mail;
    String age;
    String weight;
    String height;

    public userDetails() {
    }

    public userDetails(String name, String mail, String age, String weight, String height) {
        this.name = name;
        this.mail = mail;
        this.age = age;
        this.weight = weight;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }
}
